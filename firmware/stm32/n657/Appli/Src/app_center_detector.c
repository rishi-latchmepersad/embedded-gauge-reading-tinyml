/**
 * @brief Resample a sub-rectangle of the full YUV422 frame to 224×224 int8 RGB
 *        using simple area-average downscaling, then quantise to int8.
 *
 * @param frame_bytes    Full YUV422 frame (2592×1944×2).
 * @param frame_size     Size of frame_bytes in bytes.
 * @param src_x, src_y   Top-left of source region (full-frame coords).
 * @param src_w, src_h   Size of source region (full-frame coords).
 * @param dst            Output buffer: 224×224×3 int8 (row-major RGB).
 * @param dst_cx_out     Output buffer X stride (bytes per pixel row).
 */
static void _cd_resample_crop_to_int8(
    const uint8_t *frame_bytes, size_t frame_size,
    size_t src_x, size_t src_y,
    size_t src_w, size_t src_h,
    int8_t *dst);

/**
 * @brief Decode the int8 model output to normalised (cx, cy) in [0, 1].
 *
 * The centre-detector model outputs 2 int8 values.  Quantisation is:
 *   float_value = (int8_value - zero_point) * scale
 * where scale = 0.00390625 and zero_point = -128 (from metadata.json).
 *
 * @param output_bytes  Pointer to the raw int8 output buffer (2 bytes).
 * @param cx_out        Receives normalised centre X in [0, 1].
 * @param cy_out        Receives normalised centre Y in [0, 1].
 */
static void _cd_decode_output(
    const int8_t *output_bytes,
    float *cx_out,
    float *cy_out);

/**
 * @brief Convert a crop-space (cx, cy) to full-frame pixel coordinates.
 */
static void _cd_crop_to_full_frame(
    size_t crop_x_min, size_t crop_y_min,
    size_t crop_width, size_t crop_height,
    float crop_cx, float crop_cy,
    size_t *full_cx_out, size_t *full_cy_out);

